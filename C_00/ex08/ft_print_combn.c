#include <unistd.h>

void	ft_print_combn(int n)
{
	int	l;
	int	c;

	l = 0;
	c = 0;
	while (l < 10)
	{
		while (c < 10)
		{
			c++;
		}
		c = 0;
		l++;
	}
}
